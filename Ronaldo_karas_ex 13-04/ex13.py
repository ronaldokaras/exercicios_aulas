dia = int(input("Digite o dia da semana de 1 - 7 : "))

if dia == 1:
    print("Domingo")
elif dia == 2:
    print("segundo")
elif dia == 3:
    print("terça")
elif dia == 4:
    print("quarta")
elif dia == 5:
    print("quinta")
elif dia == 6:
    print("Sexta")
elif dia == 7:
    print("Sabado")

else:
    print("Opção invalida")
